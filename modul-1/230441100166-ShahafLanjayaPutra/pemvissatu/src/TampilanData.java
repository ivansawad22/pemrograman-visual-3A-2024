    private void jButtonTampilanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonTampilanActionPerformed
        // TODO add your handling code here:
        if(cbSyarat.isSelected()){
            Hasil hasil = new Hasil();
            hasil.jTextFieldNama.setText(jTextFieldNama.getText());
            hasil.jTextFieldTempat.setText(txtTempat.getText());
            hasil.jTextFieldTggl.setText(txtTanggal.getText());
            hasil.jTextFieldNo.setText(txtNo.getText());
            hasil.jTextFieldEmail.setText(txtEmail.getText());
            hasil.jTextFieldProdi.setText(txtProdi.getText());
            hasil.jTextFieldAlamat.setText(txtAlamat.getText());
            String Fakultas = (String)jComboBoxFakultas.getSelectedItem().toString();
            hasil.jTextFieldFakultas.setText(Fakultas);
            if (jRadioButtonLaki.isSelected()){
                hasil.jTextFieldjenis.setText("Laki-Laki");
            } else if (jRadioButtonPerempuan.isSelected()){
                hasil.jTextFieldjenis.setText("Perempuan");
            }
            hasil.setVisible(true);    
            this.dispose();
        }else {
//            lblSyarat.setText("Mohon centang terlebih dahulu");
            JOptionPane.showMessageDialog(null,"Harap Checklist Terlebih Dahulu!", "Peringatan", JOptionPane.ERROR_MESSAGE);
        }
        
        if(jTextFieldNama.getText().trim().isEmpty()||
           txtTempat.getText().trim().isEmpty()||
           txtTanggal.getText().trim().isEmpty()||
           txtAlamat.getText().trim().isEmpty()||
           txtNo.getText().trim().isEmpty()||
           txtEmail.getText().trim().isEmpty()||
           txtProdi.getText().trim().isEmpty()
                ){
            syaratNama.setText("Form harus diisi!!!");
            syaratTempat.setText("Form harus diisi!!!");
            syaratTanggal.setText("Form harus diisi!!!");
            syaratAlamat.setText("Form harus diisi!!!");
            syaratNo.setText("Form harus diisi!!!");
            syaratEmail.setText("Form harus diisi!!!");
            syaratProdi.setText("Form harus diisi!!!");
            
        }else{
            syaratNama.setText(null);
            syaratTempat.setText(null);
            syaratTanggal.setText(null);
            syaratAlamat.setText(null);
            syaratNo.setText(null);
            syaratEmail.setText(null);
            syaratProdi.setText(null);
        }
        
        
    }//GEN-LAST:event_jButtonTampilanActionPerformed

