    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        if(Validasi.isSelected()){
        formBimbel2 form2 = new formBimbel2();
        form2.Nama.setText(Nama.getText());
        form2.Tanggal_Lahir.setText(Tanggal_Lahir.getText());
        form2.Sekolah.setText(Sekolah.getText());
        form2.Kelas.setText(Kelas.getText());
        form2.Alamat.setText(Alamat.getText());
        form2.Telepon.setText(Telepon.getText());
        form2.Ortu.setText(Ortu.getText());
        // Data Kelamin        
        if(rbLaki.isSelected()==true){
            form2.kelamin.setText(rbLaki.getText());
        }
        else if(rbPerempuan.isSelected()==true){
            form2.kelamin.setText(rbPerempuan.getText());
        }else{
            form2.kelamin.setText("");
        }
        // Data Waktu        
        if(wktSatu.isSelected()==true){
            form2.Waktu.setText(wktSatu.getText());
        }
        else if(wktDua.isSelected()==true){
            form2.Waktu.setText(wktDua.getText());
        }
        else if(wktTiga.isSelected()==true){
            form2.Waktu.setText(wktTiga.getText());
        }else{
            form2.Waktu.setText("");
        }
        // Data Kursus        
        String pilihKursus = (String) Kursus.getSelectedItem().toString();
        if(pilihKursus !="--Pilih Kursus--"){
            form2.Kursus.setText(pilihKursus);
        }else{
            form2.Kursus.setText("");
        }
        String hariKursus = (String) Hari.getSelectedItem().toString();
        if(hariKursus !="--Pilihan Hari--"){
            form2.hari.setText(hariKursus);
        }else{
            form2.hari.setText("");
        }
        form2.setVisible(true);
        }else{
            JOptionPane.showMessageDialog(this,"Anda Harus Yakin!!","PAHAMM!!",JOptionPane.ERROR_MESSAGE);  
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

