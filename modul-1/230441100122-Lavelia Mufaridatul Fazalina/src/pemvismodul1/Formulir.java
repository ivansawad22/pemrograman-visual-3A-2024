    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        // TODO add your handling code here:
    if (!checkdata.isSelected()) {
    javax.swing.JOptionPane.showMessageDialog(this, "Centang kotak persetujuan sebelum disimpan!");
    return; 
    }

    Nama = tfnama.getText();
    TempatLahir = tftmpt.getText();
    Alamat = tfalamat.getText();
    Email = tfemail.getText();
    Prodi = tfprodi.getText();
    
    if (btnlk.isSelected()) {
        JenisKelamin = "Laki-Laki";
    } else if (btnpr.isSelected()){
        JenisKelamin = "Perempuan";
    }
    
    if (tffk.getSelectedIndex() == 0) { 
        Fakultas = ""; 
    } else {
        Fakultas = tffk.getSelectedItem().toString();
    }
    
    
    Data b = new Data(Nama, TempatLahir, JenisKelamin, Alamat, Email, Prodi, Fakultas);
    
      
    if (Nama.isEmpty() || TempatLahir.isEmpty() || Alamat.isEmpty() || Email.isEmpty() || Prodi.isEmpty() || JenisKelamin.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Ada form yang belum diisi!!");
        b.setVisible(false);
    }else{
        tfnama.setText(null);
        b.setVisible(true);
        dispose();
    }
             
    }//GEN-LAST:event_btnsaveActionPerformed

