    private void bKirimActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bKirimActionPerformed
        if (!ckPersetujuan.isSelected()){
            JOptionPane.showMessageDialog(this,
                    "Klik centang dulu!", "Lebih fokus yuk hehe", JOptionPane.ERROR_MESSAGE);
        }else{
            OutputForm output = new OutputForm();
            output.outNama.setText(txtNama.getText());
            output.outAlamat.setText(txtAlamat.getText());
//            Jenis Kelamin
            if (rbLakilaki.isSelected()){
                output.outJenisKelamin.setText("Laki-laki");
            }else if(rbPerempuan.isSelected()){
                output.outJenisKelamin.setText("Perempuan");
            }else{
                output.outJenisKelamin.setText("Tidak Valid");
            }
            output.outTelepon.setText(txtNotelepon.getText());
            output.outAlasan.setText(taAlasan.getText());
//            Combo Box
            int selectedIndex = cbKeahlian.getSelectedIndex();
            if (selectedIndex == 0) {
                output.outKeahlian.setText("");
            }else{
                String NilaiCombo;
                NilaiCombo = cbKeahlian.getSelectedItem().toString();
                output.outKeahlian.setText(NilaiCombo);
            }
            
            output.setVisible(true);
            this.dispose();
        }
    }//GEN-LAST:event_bKirimActionPerformed

