    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //------------percobaan
        // Mendapatkan teks dari JTextField
        String nama = NamaHal1.getText();
        String tanggalLahir = TanggalHal1.getText();
        String asal = AsalHal1.getText();
        String alamat = AlamatHal1.getText();
        String noTelfon = TeleponHal1.getText();
        String namaOrangTua = OrangtuaHal1.getText();
        String pekerjaanOrangTua = PekerjaanHal1.getText();

        
        //-----------batas akhir percobaan
        // TODO add your handling code here:
            if(validasi.isSelected()){
        Output btnSimpan = new Output();
//        btnSimpan.setVisible(true);
        btnSimpan.NamaHal2.setText(NamaHal1.getText());
        //----gender
            // Validasi Gender dengan ButtonGroup
    String gender = "";
    if (Laki.isSelected()) {
        gender = "Pria";
    } else if (Wanita.isSelected()) {
        gender = "Wanita";
    }
        //---
        
       
        //-----------------
    // Validasi ComboBox untuk Hari
    String hariKursus = HariHal1.getSelectedItem().toString();
    if (hariKursus.equals("--Pilih Hari--")) {
        JOptionPane.showMessageDialog(this, "Anda harus memilih hari kursus!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        return;  // Kembalikan eksekusi, jangan lanjutkan ke jendela baru
    }
        //----------------------
        
        //-----------------------
        // Validasi ComboBox untuk Waktu
    String waktuKursus = WaktuHal1.getSelectedItem().toString();
    if (waktuKursus.equals("--Pilih Waktu--")) {
        JOptionPane.showMessageDialog(this, "Anda harus memilih waktu kursus!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        return;  // Kembalikan eksekusi, jangan lanjutkan ke jendela baru
    }
        
        
//             Cek apakah nama kosong
    if (nama.isEmpty()) {
        // Tampilkan pesan peringatan
        JOptionPane.showMessageDialog(this, "Nama lengkap wajib diisi!", "Peringatan", JOptionPane.WARNING_MESSAGE);
//        this.dispose();
    } else if(tanggalLahir.isEmpty()){
        JOptionPane.showMessageDialog(this, "Tanggal Lahir wajib diisi!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        
    }else if(asal.isEmpty()){
        JOptionPane.showMessageDialog(this, "Asal Sekolah wajib diisi!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        
    }else if(alamat.isEmpty()){
        JOptionPane.showMessageDialog(this, "Alamat wajib diisi!", "Peringatan", JOptionPane.WARNING_MESSAGE);
    
    }else if(noTelfon.isEmpty()){
        JOptionPane.showMessageDialog(this, "No Telfon wajib diisi!", "Peringatan", JOptionPane.WARNING_MESSAGE);
    }else if(namaOrangTua.isEmpty()){
        JOptionPane.showMessageDialog(this, "Nama OrangTua wajib diisi!", "Peringatan", JOptionPane.WARNING_MESSAGE);
    }else if(pekerjaanOrangTua.isEmpty()){
        JOptionPane.showMessageDialog(this, "Pekerjaan wajib diisi!", "Peringatan", JOptionPane.WARNING_MESSAGE);
    }
    else if (gender.isEmpty()){
        JOptionPane.showMessageDialog(this, "Gender wajib diisi!", "Peringatan", JOptionPane.WARNING_MESSAGE);
      
    }
    else{
        this.dispose();
        btnSimpan.setVisible(true);
        // Lanjutkan proses penyimpanan data jika tidak kosong
        // Misalnya simpan ke database atau tampilkan di jendela lain
        // Implementasi sesuai kebutuhan
            }
    
    
        btnSimpan.TanggalHal2.setText(TanggalHal1.getText());
        btnSimpan.AsalHal2.setText(AsalHal1.getText());
        btnSimpan.AlamatHal2.setText(AlamatHal1.getText());
        btnSimpan.TeleponHal2.setText(TeleponHal1.getText());
        btnSimpan.OrangtuaHal2.setText(OrangtuaHal1.getText());
        btnSimpan.PekerjaanHal2.setText(PekerjaanHal1.getText());
        if(Laki.isSelected()== true){
            btnSimpan.JenisKelaminHal2.setText("Laki-Laki");
        }if (Wanita.isSelected() == true){
            btnSimpan.JenisKelaminHal2.setText("Perempuan");
        }
        
        String HariKursus = (String)HariHal1.getSelectedItem().toString();
        if(!"--Pilih Hari--".equals(HariKursus)){
            btnSimpan.HariHal2.setText(HariKursus);
        }else {
            btnSimpan.HariHal2.setText("");
        }
        
        //----------------------------------------
        
        String WaktuKursus = (String)WaktuHal1.getSelectedItem().toString();
        if(!"--Pilih Waktu--".equals(WaktuKursus)){
            btnSimpan.WaktuHal2.setText(WaktuKursus);
        }else {
            btnSimpan.HariHal2.setText("");
        }   
            
    }//GEN-LAST:event_jButton1ActionPerformed
            else {
                JOptionPane.showMessageDialog(this, "coba lagi", "WARNING", JOptionPane.ERROR_MESSAGE);
            }
    }
    
    
