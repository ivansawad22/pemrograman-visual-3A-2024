    private void buttonResetActionPerformed(java.awt.event.ActionEvent evt) {                                            
        // TODO add your handling code here:
        kolomNama.setText("");
        kolomTempatlahir.setText("");
        kolomTanggallahir.setText("");
        jeniskelaminbuttonGroup.clearSelection(); 
        kolomAlamat.setText("");
        kolomNotelp.setText("");
        kolomEmail.setText("");
        kolomMember.setSelectedItem("Pilih Paket Member GYM");
    }                                           

    private void buttonSimpanActionPerformed(java.awt.event.ActionEvent evt) {                                             
        // TODO add your handling code here:
        if(dataCeklis.isSelected()){
            formulirpendaftaran1 output = new formulirpendaftaran1();
            output.outputNama.setText(kolomNama.getText());
            output.outputTempatlahir.setText(kolomTempatlahir.getText());
            output.outputTanggallahir.setText(kolomTanggallahir.getText());
            if(Laki.isSelected() == true ) {
                output.outputJeniskelamin.setText("Laki-laki");
            }else if(Perempuan.isSelected() == true ) {
                output.outputJeniskelamin.setText("Perempuan");
            }
            
            output.outputAlamat.setText(kolomAlamat.getText());
            output.outputNotelp.setText(kolomNotelp.getText());
            output.outputEmail.setText(kolomEmail.getText());
            
            String pilih = kolomMember.getSelectedItem().toString();
            output.outputMember.setText(pilih);
            output.setVisible(true);
        }
    }                                            

