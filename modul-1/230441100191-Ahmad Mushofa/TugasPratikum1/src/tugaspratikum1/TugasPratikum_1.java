    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
        // TODO add your handling code here:
        
      if (!check.isSelected()) {
        JOptionPane.showMessageDialog(this,
                "Harap isi semua dulu sampai benar", "isi secara benar", JOptionPane.ERROR_MESSAGE);
    } else {
        if (jTextField1.getText().isEmpty() || jTextField2.getText().isEmpty() || jTextField3.getText().isEmpty() ||
            jTextField4.getText().isEmpty() || jTextField5.getText().isEmpty() || jTextField6.getText().isEmpty()) {
            
            javax.swing.JOptionPane.showMessageDialog(this, 
                    "Penuhi semua data sebelum melanjutkan!", 
                    "Input Tidak Lengkap", 
                    javax.swing.JOptionPane.WARNING_MESSAGE);
            
        } else if (!rdbtnlakilaki.isSelected() && !rdbtnperempuan.isSelected()) {
                javax.swing.JOptionPane.showMessageDialog(this, 
                "Pilih jenis kelamin sebelum melanjutkan!", 
                "Input Tidak Lengkap", 
                javax.swing.JOptionPane.WARNING_MESSAGE);
  
        } else if (pilihdivisi.getSelectedIndex() == -1) { 
                javax.swing.JOptionPane.showMessageDialog(this, 
                "Pilih divisi sebelum melanjutkan!", 
                "Input Tidak Lengkap", 
                javax.swing.JOptionPane.WARNING_MESSAGE);
        } else {
            String Nama = jTextField1.getText();
            String Nim = jTextField2.getText();
            String Jeniskelamin = "";
            if (rdbtnlakilaki.isSelected()) {
                Jeniskelamin = "laki-laki";
            } else if (rdbtnperempuan.isSelected()) {
                Jeniskelamin = "perempuan";
            }
            String Tanggallahir = jTextField3.getText();
            String  Alamat = jTextField4.getText();
            String Notelp = jTextField5.getText();
            String Prodi = jTextField6.getText();
            String Divisi = pilihdivisi.getSelectedItem().toString();
            
           TugasPratikum_1lanjut b = new TugasPratikum_1lanjut(Nama, Nim, Tanggallahir, Jeniskelamin, Alamat, Notelp, Divisi, Prodi);
            b.setVisible(true);
            dispose();
        }
    }
    }//GEN-LAST:event_btnsimpanActionPerformed

