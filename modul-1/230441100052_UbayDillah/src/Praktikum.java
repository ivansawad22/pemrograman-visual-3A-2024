    private void submitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitButtonActionPerformed
        // TODO add your handling code here:
        
        String name = nameForm.getText();
        String email = emailForm.getText();
        String phone = phoneForm.getText();
        String gender;
    
        if (name.isEmpty() || email.isEmpty() || phone.isEmpty() || (!man.isSelected() && !woman.isSelected()) ) {
            JOptionPane.showMessageDialog(null, "Silahkan isi semua data");
            return;
        }
        
       if (!check.isSelected()) {
            JOptionPane.showMessageDialog(null, "Pastikan data sudah benar!");
            return;
        }
        
        if (man.isSelected()) {
            gender = "Laki-laki"; 
        } else if (woman.isSelected()) {
            gender = "Perempuan"; 
        } else {
            gender = "Tidak disebutkan";
        }
        
        String education = educationsForm.getSelectedItem().toString();
        String skill = skillsForm.getSelectedItem().toString();
        
        new Result(name, email, phone, gender, education, skill).setVisible(true);
        setVisible(false);
        
    }//GEN-LAST:event_submitButtonActionPerformed

