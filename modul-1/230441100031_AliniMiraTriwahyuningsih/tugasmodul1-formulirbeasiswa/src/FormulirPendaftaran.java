    private void buttonSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSimpanActionPerformed
    if (!CekBoxCentang.isSelected()) {
        labelPeringatan.setText("Centang Dulu!");
        return;
        }
        SimpanFormulir smpn = new SimpanFormulir();
        smpn.labelnama.setText(textNama.getText());
        smpn.lebaltempatlahir.setText(textTempatLahir.getText());
        smpn.labeltanggal.setText(textTanggalLahir.getText());
        //smpn.labeljenis.setText(jRadioButton1.isSelected());
        smpn.labelAlamat.setText(textAlamat.getText());
        smpn.labelno.setText(textNoTelp.getText());
        smpn.labelEmail.setText(textEmail.getText());
        smpn.labelProdi.setText(textProdi.getText());
        smpn.labelFakultas.setText(ComboFakultas.getSelectedItem().toString());
        
        
        String jenisKelamin;
        if(jRadioButton1.isSelected()== true){
            jenisKelamin = "Laki-Laki";
        }else{
            jenisKelamin = "Perempuan";
        }
        smpn.labeljenis.setText(jenisKelamin);
        
        smpn.setVisible(true);
    }//GEN-LAST:event_buttonSimpanActionPerformed

