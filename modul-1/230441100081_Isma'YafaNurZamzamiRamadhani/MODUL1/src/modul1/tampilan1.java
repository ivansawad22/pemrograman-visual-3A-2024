    private void jButton_SimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_SimpanActionPerformed
        // TODO add your handling code here:
        
        if(jCheckBox1.isSelected()){
            Nama = jNama.getText();
            TempatLahir = jTempat_Lahir.getText();
            TanggalLahir = jTanggal_Lahir.getText();
            
            if(LakiLaki.isSelected()){
                JenisKelamin = LakiLaki.getText();
            }
            else if(Perempuan.isSelected()){
                JenisKelamin = Perempuan.getText();
            }
            else if(Lainnya.isSelected()){
                JenisKelamin = Lainnya.getText();
            }
            else{
                JenisKelamin = " ";
            }
            
            NoTelp = jNo_Telp.getText();
            Alamat = jAlamat.getText();
            Pilihan = jPilihan.getSelectedItem().toString();
            
            if(Pilihan != "Ultraman"){
                Pilihan = jPilihan.getSelectedItem().toString();
            }
            else{
                Pilihan = "";
            }
            
            Email = jEmail.getText();
            Alasan = jAlasan.getText();
            
            Tampilan2 tampilan2 = new Tampilan2();
            tampilan2.setVisible(true);
            dispose();
            
        }
        else{
            JOptionPane.showMessageDialog(this, "KAMU TIDAKK YAKINN!!! SILAHKAN YAKIN DULUU", "WARNINGGGG!!!!", JOptionPane.WARNING_MESSAGE);
        }
        
        
    }//GEN-LAST:event_jButton_SimpanActionPerformed

