    private void ButtonSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonSimpanActionPerformed
        // TODO add your handling code here:
        String Nama = TextFieldNama.getText();
        String NIM = TextFieldNIM.getText();
        String TempatTanggalLahir = TextFieldTTL.getText();
        String AlamatAsal = TextFieldAlamat.getText();
        String Prodi = TextFieldProdi.getText();
        String NomorTelepon = TextFieldNomor.getText();
        String Email = TextFieldEmail.getText();
        String Agama = TextFieldAgama.getText();
        String JenisKelamin = RadioButtonLakilaki.isSelected()? "Laki-laki":
                RadioButtonPerempuan.isSelected()? "Perempuan": "";
        String Fakultas = ComboBoxFakultas.getSelectedItem().toString();
        if (!CheckBoxData.isSelected()){
            JOptionPane.showMessageDialog(this,"centang checkbox untuk melanjukan.", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }
        Frame2 frame2 = new Frame2();
        frame2.setData (Nama, NIM, TempatTanggalLahir, AlamatAsal, Prodi, NomorTelepon, Email, Agama, JenisKelamin, Fakultas);
        frame2.setVisible(true);
    }//GEN-LAST:event_ButtonSimpanActionPerformed

