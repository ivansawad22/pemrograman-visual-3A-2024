    private void textCashActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_textCashActionPerformed
        // TODO add your handling code here:
        try{
            double total = Double.parseDouble(textTotal.getText());
            double bayar = Double.parseDouble(textCash.getText());
            double diskon = 0;
// cek apakah total pembelian diatas 100.000 untuk mendapatkan diskon 5%
            if(total >= 100000){
                diskon = total * 0.05;
                total = total - diskon;
        }
            //        cek apakah uang pembayaran negatif
        if(bayar < 0){
            JOptionPane.showMessageDialog(null, "Uang yang dibayarkan tidak boleh negatif.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
//            cek apakah uang pembayaran cukup
        if (bayar < total){
            double kurang = total - bayar;
            JOptionPane.showMessageDialog(null, "Uang Yang Dibayarkan Kurang. Anda masih kurang Rp. " + kurang + "Silahkan masukkan jumlah yang cukup.", "Error", JOptionPane.ERROR_MESSAGE);
            return; //keluar dari metode jika uang tidak cukup
        }
//        cek apakah uang pembayaran negatif
//        if(bayar < 0){
//            JOptionPane.showMessageDialog(null, "Uang yang dibayarkan tidak boleh negatif.", "Error", JOptionPane.ERROR_MESSAGE);
//            return;
//        }
            double kembalian = bayar - total;
////            mengatur format angka dengan 2 digit desimal      
//            DecimalFormat df = new DecimalFormat("#.##");
            textKembali.setText(String.valueOf(kembalian));
//            asumsikan ada variabel yang menyimpan detail menu
        String menuDipilih = "";
        if(btnOriginal.isSelected()){
            menuDipilih = "Original";
        }else if (btnSpicy.isSelected()){
            menuDipilih = "Spicy";
        }else if(btnHoneyGarlic.isSelected()){
            menuDipilih = "Korean";
        }else if(btnCheese.isSelected()){
            menuDipilih = "Cheese";
        }else if(btnSaltedEgg.isSelected()){
            menuDipilih ="Salted Egg";
        }else if(btnBlackpepper.isSelected()){
            menuDipilih ="Blackpepper";
        }else if(btnBbq.isSelected()){
            menuDipilih = "BBQ";    
        }
        String toppingDipilih = "";
        if(btnSambalmatah.isSelected()){
            toppingDipilih += "Sambal Matah";
        }
        if(btnMayones.isSelected()){
            toppingDipilih += "Mayones Pedas";
        }
        if(btnBawang.isSelected()){
            toppingDipilih += "Bawang Goreng";
        }
        if(btnKeju.isSelected()){
            toppingDipilih += "Keju Parut";
        }
        if(btnNotopping.isSelected()){
            toppingDipilih += "No Topping";
            
        }
            double uangDibayar = bayar;
            JOptionPane.showMessageDialog(null, "Pembayaran berhasil dilakukan.\nMenu Yang Dipilih : "+ menuDipilih + "\nTopping : " + toppingDipilih +  "\nTotal Harga : Rp. "+ total + "\nUang dibayar : Rp. " +uangDibayar + "\nKembalian: Rp. " + kembalian, "Informasi Pemesanan!",JOptionPane.INFORMATION_MESSAGE, iconSukses);
        }catch (NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Input tidak valid. Pastikan memasukkan angka yang benar.", "Error", JOptionPane.ERROR);
        }
    }//GEN-LAST:event_textCashActionPerformed

