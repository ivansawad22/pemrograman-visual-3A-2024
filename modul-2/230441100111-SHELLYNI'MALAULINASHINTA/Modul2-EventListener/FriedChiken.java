    private void buttonTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonTambahActionPerformed
        // TODO add your handling code here:
//        Ambil nilai saat ini dari text field
        String currentText = textJumlah.getText();
        int jumlahPesanan = 0;
//        periksa apakah teks field tidak kosong dan hanya berisi angka
        if(!currentText.isEmpty() && currentText.matches("\\d+")){
//            parse nilai integer jika valid
              jumlahPesanan = Integer.parseInt(currentText);
        }
//        Tambah jumlah pesanan
        jumlahPesanan++;
//        set nilai baru ke text field
        textJumlah.setText(String.valueOf(jumlahPesanan));
    }//GEN-LAST:event_buttonTambahActionPerformed

