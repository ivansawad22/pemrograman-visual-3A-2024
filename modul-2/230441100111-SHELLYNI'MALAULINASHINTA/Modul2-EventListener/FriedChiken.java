    private void buttonTotalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonTotalActionPerformed
        // TODO add your handling code here:
        int harga = 0;
        double diskon = 0;
        double totalHarga = 0;
        boolean menuDipilih = false;
        boolean toppingDipilih = false;
        if (btnOriginal.isSelected()){
            harga = 15000;
            menuDipilih = true;
        }else if (btnSpicy.isSelected()){
            harga = 16000;
            menuDipilih = true;
        }else if (btnHoneyGarlic.isSelected()){
            harga = 19000;
            menuDipilih = true;
        }else if (btnKorean.isSelected()){
            harga = 20000;
            menuDipilih = true;
        }else if (btnCheese.isSelected()){
            harga = 18000;
            menuDipilih = true;
        }else if (btnSaltedEgg.isSelected()){
            harga = 21000;
            menuDipilih = true;
        }else if (btnBlackpepper.isSelected()){
            harga = 17000;
            menuDipilih = true;
        }else if (btnBbq.isSelected()){
            harga = 18500;
            menuDipilih = true;
        }
        String jumlahText = textJumlah.getText();
        if(jumlahText.isEmpty()){
            JOptionPane.showMessageDialog(this, "Silahkan Masukkan Jumlah!", "Peringatan", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        int jumlah = Integer.parseInt(jumlahText);
//        menghitung total harga sebelum diskon
        totalHarga = harga * jumlah;
        
//        menambahkan total harga topping jika dipilih
        if(btnNotopping.isSelected()){
            toppingDipilih = true;
        }else if (btnSambalmatah.isSelected()){
            totalHarga += 4000*jumlah;
            toppingDipilih = true;
        }else if (btnMayones.isSelected()){
            totalHarga += 3500*jumlah;
            toppingDipilih = true;
        }else if (btnBawang.isSelected()){
            totalHarga += 2500*jumlah;
            toppingDipilih = true;
        }else if(btnKeju.isSelected()){
            totalHarga += 5000*jumlah;
            toppingDipilih = true;
        }
        txtdiscon.setText(String.valueOf(totalHarga));
        if(!menuDipilih){
            JOptionPane.showMessageDialog(this, "Silahkan Pilih Menu Terlebih Dahulu!", "Peringatan", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        if(!toppingDipilih){
            JOptionPane.showMessageDialog(this, "Silahkan Pilih Topping Terlebih Dahulu!", "Peringatan", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
//        menerapkan diskon jika total harga lebih dari 100.000
        
        if(totalHarga >= 100000){
            diskon = totalHarga * 0.05;
            totalHarga -= diskon;
            JOptionPane.showMessageDialog(this, "Anda Mendapatkan Diskon 5%! Diskon: Rp " + (int)diskon );
        }
//        menampilkan total harga setelah diskon
        textTotal.setText(String.valueOf(totalHarga));
    }//GEN-LAST:event_buttonTotalActionPerformed

