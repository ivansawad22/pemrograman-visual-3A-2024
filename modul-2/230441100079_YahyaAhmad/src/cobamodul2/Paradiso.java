    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
    if (!jRadioButton1.isSelected() && !jRadioButton2.isSelected() && !jRadioButton3.isSelected() && !jRadioButton4.isSelected() && !jRadioButton5.isSelected() && !jRadioButton6.isSelected() && !jRadioButton7.isSelected()) {
        JOptionPane.showMessageDialog(this, "Pilih Salah Satunya Bro, jangan dikosongin", "WARNING", JOptionPane.WARNING_MESSAGE);
    } else {  
        if (jRadioButton1.isSelected()) {
            hargaMakanan = 65000;
        } else if (jRadioButton2.isSelected()) {
            hargaMakanan = 45000;
        } else if (jRadioButton3.isSelected()) {
            hargaMakanan = 50000;
        } else if (jRadioButton4.isSelected()) {
            hargaMakanan = 52000;
        } else if (jRadioButton5.isSelected()) {
            hargaMakanan = 30000;
        } else if (jRadioButton6.isSelected()) {
            hargaMakanan = 55000;
        } else if (jRadioButton7.isSelected()) {
            hargaMakanan = 70000;
        } else{
            hargaMakanan = 0;
        }

        if (jRadioButton8.isSelected()) {
            hargaTopping = 10000;
        } else if (jRadioButton9.isSelected()) {
            hargaTopping = 25000;
        } else if (jRadioButton10.isSelected()) {
            hargaTopping = 5000;
        }else{
            hargaTopping = 0;
        }

        totalSemua = (hargaMakanan + hargaTopping) * jumlahAnu;
        if (totalSemua > 100000) {
            totalSemua = totalSemua - (totalSemua * 5 / 100);
        }
        jTextField1.setText(Integer.toString(totalSemua));

        int hasilbuatJTF3 = Integer.parseInt(jTextField3.getText());
        int hasil = hasilbuatJTF3 - totalSemua;

        jTextField2.setText(Integer.toString(hasil));
        }
    }//GEN-LAST:event_jButton1ActionPerformed

