    private void hitungKembalian() {
            try{
            int HargaFix = Integer.parseInt(TotalHarga.getText());
            int uang = Integer.parseInt(Bayar.getText());
            int kembalian =  uang - HargaFix ;
        
            if (kembalian >=0){
                Kembali.setText("Kembali = "+kembalian);
                System.out.println("\nHarga Pembelian  \t\t:"+HargaFix+"\nUang Diberikan \t\t\t: "+uang+ "\nKembalian   \t\t\t:"+kembalian);
            }else if(kembalian <0){
                Kembali.setText("Uang Kurang");
            }else{
                System.out.println("cihuyyy");
            }
            }catch (NumberFormatException e) {
                Kembali.setText("");
        }
    }
    
    

