    private void hitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hitungActionPerformed
        // TODO add your handling code here:
        if (!coklat.isSelected() && !keju.isSelected() && !matcha.isSelected() && !coklatkeju.isSelected()
            && !coklatkeju.isSelected() && !strawberrykeju.isSelected() &&
        !blueberry.isSelected() && !strawberry.isSelected() && !kacang.isSelected()) {
        JOptionPane.showMessageDialog(this, "Silakan pilih jenis roti terlebih dahulu!", "Warning", JOptionPane.WARNING_MESSAGE);
        return;
        }
        
        if (!susu.isSelected() && !sosis.isSelected() && !mayo.isSelected() && !almond.isSelected()
              && !abon.isSelected() && !dagingsuwir.isSelected()) {
        JOptionPane.showMessageDialog(this, "Silakan pilih topping terlebih dahulu!","Warning", JOptionPane.WARNING_MESSAGE);
        return;
        }
        
        if(coklat.isSelected()){
            hargaRoti = 12000;
        }else if(keju.isSelected()){
            hargaRoti = 15000;
        }else if(matcha.isSelected()){
            hargaRoti = 18000;
        }else if(blueberry.isSelected()){
            hargaRoti = 18000;
        }else if(strawberry.isSelected()){
            hargaRoti = 12000;
        }else if(kacang.isSelected()){
            hargaRoti = 18000;
        }else if(coklatkeju.isSelected()){
            hargaRoti = 18000;
        }else if(coklatkacang.isSelected()){
            hargaRoti = 18000;
        }else if(strawberrykeju.isSelected()){
            hargaRoti = 18000;
        }
        
        if(susu.isSelected()){
            hargaTopping = 3000;
        }else if(sosis.isSelected()){
            hargaTopping = 5000;
        }else if(mayo.isSelected()){
            hargaTopping = 7000;
        }else if(dagingsuwir.isSelected()){
            hargaTopping = 8000;
        }else if(almond.isSelected()){
            hargaTopping = 12000;
        }else if(abon.isSelected()){
            hargaTopping = 8000;
        }
        
        double totalHarga = (hargaRoti + hargaTopping)*jumlahPesanan;
        
        //Penerapan diskon jika total harga lebih dari 100000
        if(totalHarga > 100000){
            totalHarga*=0.95; // Diskon 5%
        }
         
        total.setText(String.format("Rp %.2f", totalHarga));
        total.setEditable(false);
        
        // Validasi input uang
        if (cash.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Masukkan uangnya terlebih dahulu!",  "Peringatan", JOptionPane.ERROR_MESSAGE,iconX);
            return;
        }

        // Menghitung kembalian
        try {
            double uangPembeli = Double.parseDouble(cash.getText());
            double kembalian = uangPembeli - totalHarga;

            // Menampilkan kembalian dengan 2 angka di belakang koma
            kembali.setText(String.format("Rp %.2f", kembalian));
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Masukkan jumlah uang yang valid!");
        }
      
    }//GEN-LAST:event_hitungActionPerformed

