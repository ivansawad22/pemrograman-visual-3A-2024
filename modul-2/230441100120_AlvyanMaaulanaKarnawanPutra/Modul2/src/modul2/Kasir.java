    private void TotalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_TotalActionPerformed
        // TODO add your handling code here:
        int menuMinuman = 0;
        int menuCamilan = 0;
        int Diskon = 0;
        int TotalHarga = 0; 

        if (buttonGroup1.getSelection() == null && buttonGroup2.getSelection() == null) {
            JOptionPane.showMessageDialog(this, "Silahkan Pesan Dulu !!", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (buttonGroup1.getSelection() == null) {
            JOptionPane.showMessageDialog(this, "Silahkan Pesan Minuman Dulu !!", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (buttonGroup2.getSelection() == null) {
            JOptionPane.showMessageDialog(this, "Silahkan Pesan Camilan Dulu !!", "Warning", JOptionPane.WARNING_MESSAGE);
        }

        if (list1.isSelected()) {
            menuMinuman = Jumlah * 19000;
        } else if (list2.isSelected() || list3.isSelected() || list4.isSelected()) {
            menuMinuman = Jumlah * 25000;
        } else if (list5.isSelected() || list6.isSelected() || list7.isSelected() || list8.isSelected()) {
            menuMinuman = Jumlah * 35000;
        }

        if (list9.isSelected() || list10.isSelected() || list11.isSelected()) {
            menuCamilan += Jumlah * 19000;
        } else if (list12.isSelected()) {
            menuCamilan += Jumlah * 29000;
        }

        TotalHarga = menuMinuman + menuCamilan;
        HargaAwal = TotalHarga; 

        if (TotalHarga > 100000) {
            Diskon = TotalHarga * 5 / 100;
            TotalHarga -= Diskon;
        JOptionPane.showMessageDialog(this, "Anda dapat potongan 5%", "Information", JOptionPane.INFORMATION_MESSAGE);
        }


        Hasil.setText("Rp." + TotalHarga);
       

        
    }//GEN-LAST:event_TotalActionPerformed

