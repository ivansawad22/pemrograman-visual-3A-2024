    private void cashKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_cashKeyReleased
        // TODO add your handling code here:
        try{
            int HargaTotal = Integer.parseInt(Hasil.getText().replace("Rp.", ""));
            int Cash = Integer.parseInt(cash.getText());
            int a = HargaAwal;
            int Kembalian = Cash - HargaTotal;
            String nama = (Jnama.getText());
            int diskon = a-HargaTotal;
            
            if(Cash < HargaTotal){
                kembali.setText("Uang Anda Kurang");
            }else {  
                kembali.setText("Rp."+Kembalian);
                JOptionPane.showMessageDialog(null, 
                "Pembayaran berhasil dilakukan.\nTotal nama :" + nama+
                "\nDiskon: Rp." +  diskon+
                "\nHarga Diskon: Rp. " + HargaTotal +
                "\nUang dibayar: Rp. " + Cash +
                "\nKembalian: Rp. " + Kembalian,
                "Informasi Pemesanan!",
                
                JOptionPane.INFORMATION_MESSAGE, iconSukses);
                
            }
            
        }catch(NumberFormatException e){
            kembali.setText("Masukkan Angka");
        }
        
        
    }//GEN-LAST:event_cashKeyReleased

