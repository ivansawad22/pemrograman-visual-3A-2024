    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int hargaIceCream = 0;
        int hargaTopping = 0;
        int hargaTotal = 0;
        int diskon = 0;
        
        if(Menu1.isSelected()){
            hargaIceCream = jumlah * 15000;
        }else if (Menu2.isSelected()){
            hargaIceCream = jumlah * 21000;
        }else if (Menu3.isSelected()){
            hargaIceCream = jumlah * 12000;
        }else if (Menu4.isSelected()){
            hargaIceCream = jumlah * 11000;
        }else if (Menu5.isSelected()){
            hargaIceCream = jumlah * 19000;
        }else if (Menu6.isSelected()){
            hargaIceCream = jumlah * 31000;
        }else {
            JOptionPane.showMessageDialog(this, "Pilih Menu Ice Cream terlebih dahulu!", "Peringatan", JOptionPane.WARNING_MESSAGE,coba);
        }
        
        if(Topping1.isSelected()){
            hargaTopping = jumlah * 6000;
        }else if(Topping2.isSelected()){
            hargaTopping = jumlah * 8000;
        }else if(Topping3.isSelected()){
            hargaTopping = jumlah * 9000;
        }else {
            JOptionPane.showMessageDialog(this, "Pilih Topping Ice Cream terlebih dahulu!", "Peringatan", JOptionPane.WARNING_MESSAGE,cancel);
        }
        
        hargaTotal = hargaIceCream + hargaTopping;
    
        if (hargaTotal >= 100000){
            diskon = hargaTotal * 5/100;
            hargaTotal -= diskon;
            JOptionPane.showMessageDialog(this, "Berhasil Mendapatkan diskon 5000!", "Peringatan", JOptionPane.WARNING_MESSAGE,sukses);
        }
        Total.setText(String.valueOf(hargaTotal));
    }//GEN-LAST:event_jButton3ActionPerformed

