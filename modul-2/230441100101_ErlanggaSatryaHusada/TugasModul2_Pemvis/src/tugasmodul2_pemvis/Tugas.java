    private void jumlahCashKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jumlahCashKeyReleased
        // TODO add your handling code here:
        try{
            int hargaTotal = Integer.parseInt(Total.getText());
            int Cash = Integer.parseInt(jumlahCash.getText());
            
            if (Cash < hargaTotal){
                Kembalian.setText("Uang anda kurang");
            } else{
                int kembalian = Cash - hargaTotal;
                Kembalian.setText(String.valueOf(kembalian));
            }
        }catch (NumberFormatException e) {
            Kembalian.setText("Masukkan angka saja");
        }
            
            
    }//GEN-LAST:event_jumlahCashKeyReleased

