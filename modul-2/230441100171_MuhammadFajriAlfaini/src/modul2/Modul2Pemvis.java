    private void CashKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_CashKeyReleased
        // TODO add your handling code here:
        try{
        int Harga_total = Integer.parseInt(Total.getText());
        int cash = Integer.parseInt(Cash.getText());
        
       
        if (cash < Harga_total){
            Kembalian.setText("Uang anda kurang");
        } else {
            int kembalian = cash - Harga_total;
            Kembalian.setText(String.valueOf(kembalian));        
        }    
        } catch (NumberFormatException e) {
            Kembalian.setText("Masukkan angka saja");
        } 
    }//GEN-LAST:event_CashKeyReleased
     
