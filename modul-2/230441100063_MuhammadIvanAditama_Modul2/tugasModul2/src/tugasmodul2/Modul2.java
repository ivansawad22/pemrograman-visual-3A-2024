    private void btnTotalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTotalActionPerformed
        // TODO add your handling code here:
        int a = 0;
        int b = 0;
        int c;
        if(tebalOri.isSelected()){
            a = 14000; 
        }else if(tebalPandan.isSelected()){
            a = 15000;
        }else if(tipisOri.isSelected()){
            a = 10000;
        }else if(tipisPandan.isSelected()){
            a = 11000;
        }else{
            JOptionPane.showMessageDialog(null, "Harap isi menu terlebih dahulu", "Peringatan", JOptionPane.INFORMATION_MESSAGE , iconSukses);
        }
        
        if(coklat.isSelected()){
            b = 3000;
        }else if(keju.isSelected()){
            b = 3000;
        }else if(coklatKeju.isSelected()){
            b = 5000;
        }else if(coklatKacang.isSelected()){
            b = 5000;
        }else if(strawberry.isSelected()){
            b = 4000;
        }else if(greentea.isSelected()){
            b = 4000;
        }else{
            JOptionPane.showMessageDialog(null, "Harap pilih topping dulu", "Peringatan", JOptionPane.INFORMATION_MESSAGE , iconSukses);
        }
        c =  a +  b;
        int nilai = Integer.parseInt(txtJumlah.getText());
        int d = c * nilai;
        txtTotal.setText(Integer.toString(d));
    }//GEN-LAST:event_btnTotalActionPerformed

