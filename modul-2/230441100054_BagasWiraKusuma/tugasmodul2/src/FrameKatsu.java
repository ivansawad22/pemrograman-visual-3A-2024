    private void updateKembalian() {
        String cashText = jumlahCash.getText();
        int totalHarga = 0; // Pastikan ini diisi dari hasilTotal

        // Mendapatkan total dari hasilTotal
        if (!hasilTotal.getText().isEmpty()) {
            totalHarga = Integer.parseInt(hasilTotal.getText());
        }

        // Menghitung kembalian
        if (!cashText.isEmpty()) {
            try {
                int cash = Integer.parseInt(cashText);
                int kembalianValue = cash - totalHarga;
                kembalian.setText(String.valueOf(kembalianValue));
            } catch (NumberFormatException e) {
                kembalian.setText("0");
            }
        } else {
            kembalian.setText("0");
        }
    }

