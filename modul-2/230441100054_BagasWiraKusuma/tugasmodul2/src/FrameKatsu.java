    private void tambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tambahActionPerformed
    // Mendapatkan nilai dari text field jumlah
    int currentJumlah;

    // Jika text field jumlah kosong, set currentJumlah ke 0
    if (jumlah.getText().isEmpty()) {
        currentJumlah = 0;
    } else {
        // Mengonversi nilai yang ada di text field menjadi integer
        currentJumlah = Integer.parseInt(jumlah.getText());
    }
    
    // Menambah jumlah pesanan
    currentJumlah++;
    
    // Memperbarui nilai pada text field jumlah
    jumlah.setText(String.valueOf(currentJumlah));
    }//GEN-LAST:event_tambahActionPerformed

    private void kurangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_kurangActionPerformed
    // Mendapatkan nilai dari text field jumlah
    int currentJumlah = Integer.parseInt(jumlah.getText());

    // Mengurangi jumlah pesanan jika lebih dari 0
    if (currentJumlah > 1) {
        currentJumlah--;
    }
    
    // Memperbarui nilai pada text field jumlah
    jumlah.setText(String.valueOf(currentJumlah));
    }//GEN-LAST:event_kurangActionPerformed

    private void totalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_totalActionPerformed

    // Mendapatkan jumlah pesanan dari text field jumlah
    int jumlahPesanan = Integer.parseInt(jumlah.getText());
    
    // Mendefinisikan harga jajanan pasar dan topping
    int totalHarga = 0;
    int diskon = 0;
    int hargaTopping = 0;

    // Mengecek pilihan jajanan pasar
    if (OriKatsu.isSelected()) {
        totalHarga = 15000 * jumlahPesanan; 
    } else if (SpKatsu.isSelected()) {
        totalHarga = 17000 * jumlahPesanan; 
    } else if (GyuKatsu.isSelected()) {
        totalHarga = 25000 * jumlahPesanan; 
    } else if (Fillet.isSelected()) {
        totalHarga = 12000 * jumlahPesanan; 
    } else if (FriedChick.isSelected()) {
        totalHarga = 9000 * jumlahPesanan; 
    } else if (Gyoza.isSelected()) {
        totalHarga = 8000 * jumlahPesanan; 
    } else {
        // Menampilkan pop-up peringatan jika tidak ada jajanan yang dipilih
        JOptionPane.showMessageDialog(null, "Mohon isi Pesanan Terlebih Dahulu!", "Gagal", JOptionPane.INFORMATION_MESSAGE, iconFailed);
        return; // Keluar dari metode jika tidak ada jajanan yang dipilih
    }

    if (Curry.isSelected()) {
        hargaTopping += 5000 * jumlahPesanan;
    } else if(Nashville.isSelected()) {
        hargaTopping += 5000 * jumlahPesanan;
    }

    // Menambahkan harga topping ke total harga
    totalHarga += hargaTopping;

    // Mengecek apakah total harga melebihi Rp. 100.000 untuk diskon
    if (totalHarga > 100000) {
        diskon = totalHarga * 5 / 100; // Menghitung diskon 5%
        totalHarga -= diskon; // Mengurangi total dengan diskon
    }

    // Menampilkan total harga pada text field hasilTotal
    hasilTotal.setText(String.valueOf(totalHarga));
    }//GEN-LAST:event_totalActionPerformed

