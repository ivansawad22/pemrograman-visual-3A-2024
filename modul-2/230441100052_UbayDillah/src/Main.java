    private void totalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_totalActionPerformed
        // TODO add your handling code here:
        int priceMartabak = 0; 
        int priceToping = 0;
        int price;
        int total;
        int totalPrice;
        
        if (MOriginal.isSelected()) {
            priceMartabak = 20000;
        } else if (MRedVelvet.isSelected()) {
            priceMartabak = 25000;
        } else if (MTaro.isSelected()){
            priceMartabak = 25000;
        } else if (MKetanHitam.isSelected()) {
            priceMartabak = 25000;
        } else if (MPandan.isSelected()) {
            priceMartabak = 25000;
        } else if (MTiramisu.isSelected()) {
            priceMartabak = 27000;
        } else {
            priceMartabak = 0;
        }
        
        if (cokelat.isSelected()) {
            priceToping = 5000;
        } else if (kelapa.isSelected()) {
            priceToping = 5000;
        } else if (kacang.isSelected()) {
            priceToping = 5000;
        } else if (keju.isSelected()) {
            priceToping = 7000;
        } else {
            priceToping = 0;
        }
        
        if (priceMartabak == 0 || priceToping == 0) {
            JOptionPane.showMessageDialog(null, "Silahkan lengkapi pesanan", "Warning!", JOptionPane.INFORMATION_MESSAGE, icon);
            return;
        }
        
        total = Integer.parseInt(jumlah.getText());
        price = priceMartabak + priceToping;
        totalPrice = total * price;
        
        if (totalPrice > 100000) {
            discount.setVisible(true);
            totalPrice = totalPrice * 95 / 100;
        } else {
            discount.setVisible(false);
        }
        
        hasilTotal.setText(Integer.toString(totalPrice));
    }//GEN-LAST:event_totalActionPerformed

