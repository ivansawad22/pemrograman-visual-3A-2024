    private void cashKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_cashKeyReleased
        String textCash = cash.getText();
        String textTotal = hasilTotal.getText();

        if (textCash.isEmpty() || textTotal.isEmpty()) {
            return;
        }
        
        int cash = Integer.parseInt(textCash);
        int total = Integer.parseInt(textTotal);
        
        int kembalian = cash-total;
        
        if (kembalian < 0) {
            kembalian = 0;
        }
        
        
        kembali.setText(Integer.toString(kembalian));
        
//        kembali.setText(Integer.toString(total - cashAmount));
    }//GEN-LAST:event_cashKeyReleased

