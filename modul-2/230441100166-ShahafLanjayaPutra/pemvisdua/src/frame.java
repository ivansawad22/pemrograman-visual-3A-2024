    private void jButtonTotalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonTotalActionPerformed
        // TODO add your handling code here:
        int Harga = 0;
if (jRadioButtonThaiThea.isSelected()) {
    Harga = 1000; 
} else if (jRadioButtonChocoChese.isSelected()) {
    Harga = 2000; 
} else if (jRadioButtonGreenTea.isSelected()) {
    Harga = 3000; 
} else if (jRadioButtonbangbang.isSelected()) {
    Harga = 4000; 
} else if (jRadioButtonLemonTea.isSelected()) {
    Harga = 5000; 
} else if (jRadioButtonMilkshake.isSelected()) {
    Harga = 6000;
} else if (jRadioButtonMatcha.isSelected()) {
    Harga = 7000; 
} else if (jRadioButtonIcedMocha.isSelected()) {
    Harga = 100000; 
} else {
    JOptionPane.showMessageDialog(this, "Harap Pilih Roti Terlebih Dahulu.", "Silahkan Pilih!!", JOptionPane.WARNING_MESSAGE);
    return;
}

int ToppingHarga = 0;
if (jRadioButtonwhipped.isSelected()) {
    ToppingHarga = 0; // Tidak ada tambahan biaya
} else if (jRadioButtonSyrupVanilla.isSelected()) {
    ToppingHarga = 1000; 
} else if (jRadioButtonOreo.isSelected()) {
    ToppingHarga = 2000; 
} else {
    JOptionPane.showMessageDialog(this, "Harap pilih topping terlebih dahulu.", "Silahkan Pilih!!", JOptionPane.WARNING_MESSAGE);
    return;
}

if (jTextFieldJumlah.getText().isEmpty()) {
    JOptionPane.showMessageDialog(this, "Harap isi jumlah terlebih dahulu.", "Silahkan Isi!!", JOptionPane.WARNING_MESSAGE);
    return;
}
        if (jTextFieldJumlah.getText().isEmpty() || jTextFieldCash.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Harap Pilih Menu Terlebih Dahulu", "Silahkan Pilih!!", JOptionPane.WARNING_MESSAGE);
            return;
    }
        try {
            int Cash = Integer.parseInt(jTextFieldCash.getText());
            int Jumlah = Integer.parseInt(jTextFieldJumlah.getText());
            
            int Total = Harga * Jumlah;
            if (Total > 100000) {
                Total*= 0.95;
            }
            int Kembali = Cash - Total;
            jTextFieldTotal.setText(Integer.toString(Total));
            jTextFieldKembali.setText(Integer.toString(Kembali));
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Masukan Harga Yang Benar", "Silahkan Masukan Harga", JOptionPane.WARNING_MESSAGE);
        
          
        }
    }//GEN-LAST:event_jButtonTotalActionPerformed

