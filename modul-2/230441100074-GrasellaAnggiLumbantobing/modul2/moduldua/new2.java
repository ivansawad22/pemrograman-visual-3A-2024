    private void TextFieldCashActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_TextFieldCashActionPerformed
        // TODO add your handling code here:
        try{
            double total = Double.parseDouble(TextFieldTotal.getText());
            double bayar = Double.parseDouble(TextFieldCash.getText());
            double diskon = 0;

            if(total > 100000){
                diskon = total * 0.05;
                total = total - diskon;
        }
        if (bayar < total) {
            JOptionPane.showMessageDialog(null, "Uang Yang Dibayar Kurang. Silahkan Masukkan Jumlah Yang Cukup!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
            double kembalian = bayar - total;
            TextFieldKembalian.setText(String.valueOf(kembalian));

        String menuDipilih = "";
        if(RadioButtonVanila.isSelected()){
            menuDipilih = "Vanila";
        }else if (RadioButtonChocolate.isSelected()){
            menuDipilih = "Chocolate";
        }else if(RadioButtonStrawberry.isSelected()){
            menuDipilih = "Strawberry";
        }else if(RadioButtonMatcha.isSelected()){
            menuDipilih = "Matcha";
        }else if(RadioButtonMango.isSelected()){
            menuDipilih ="Mango";
        }else if(RadioButtonBlueberry.isSelected()){
            menuDipilih ="Blueberry";
        }else if(RadioButtonCappucino.isSelected()){
            menuDipilih = "Cappucino";    
        }else if(RadioButtonGrape.isSelected()){
            menuDipilih = "Grape";    
        }
        String toppingDipilih = "";
        if(RadioButtonCeres.isSelected()){
            toppingDipilih += "Ceres";
        }
        if(RadioButtonOreo.isSelected()){
            toppingDipilih += "Oreo";
        }
        if(RadioButtonKoko.isSelected()){
            toppingDipilih += "Koko";
        }
        if(RadioButtonMarshmallow.isSelected()){
            toppingDipilih += "Marshmallow";
        }
        if(RadioButtonTanpaTopping.isSelected()){
            toppingDipilih += "Tanpa Topping";
            
        }
            double uangDibayar = bayar;
            JOptionPane.showMessageDialog(null, "Pembayaran berhasil dilakukan.\nMenu Yang Dipilih : "+ menuDipilih + "\nTopping : " + toppingDipilih +  "\nTotal Harga : Rp. "+ total + "\nUang dibayar : Rp. " +uangDibayar + "\nKembalian: Rp. " + kembalian, "Penambahan menu",JOptionPane.INFORMATION_MESSAGE, iconSukses);
        }catch (NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Input tidak valid. Pastikan memasukkan angka yang benar.", "Error", JOptionPane.ERROR);
        } 
    }//GEN-LAST:event_TextFieldCashActionPerformed
        
