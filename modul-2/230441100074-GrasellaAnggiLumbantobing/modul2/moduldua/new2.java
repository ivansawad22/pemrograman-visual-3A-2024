    private void ButtnTotalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtnTotalActionPerformed
        // TODO add your handling code here:
        int harga = 0;
        double diskon = 0;
        double totalHarga = 0;
        boolean pilihanMenu = false;
        boolean pilihanTopping = false;
        if (RadioButtonVanila.isSelected()) {
            harga = 8000;
            pilihanMenu = true;
        }else if (RadioButtonStrawberry.isSelected()) {
            harga = 9000;
            pilihanMenu = true;
        }else if (RadioButtonCappucino.isSelected()) {
            harga = 10000;
            pilihanMenu = true;
        }else if (RadioButtonChocolate.isSelected()) {
            harga = 10000;
            pilihanMenu = true;
        }else if (RadioButtonMatcha.isSelected()) {
            harga = 12000;
            pilihanMenu = true;
        }else if (RadioButtonBlueberry.isSelected()) {
            harga = 12000;
            pilihanMenu = true;
        }else if (RadioButtonMango.isSelected()) {
            harga = 12000;
            pilihanMenu = true;
        }else if (RadioButtonGrape.isSelected()) {
            harga = 14000;
            pilihanMenu = true;
        }
        String jumlahText = TextFieldJumlah.getText();
        if (jumlahText.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Silahkan Masukkan Jumlah", "Perngatan", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        int jumlah = Integer.parseInt(jumlahText);
        totalHarga = harga * jumlah;
        if (RadioButtonTanpaTopping.isSelected()) {
            pilihanTopping = true;
        }else if (RadioButtonCeres.isSelected()) {
            totalHarga += 2000*jumlah;
            pilihanTopping = true;
        }else if (RadioButtonOreo.isSelected()) {
            totalHarga += 2000*jumlah;
            pilihanTopping = true;
        }else if (RadioButtonKoko.isSelected()) {
            totalHarga += 2000*jumlah;
            pilihanTopping = true;
        }else if (RadioButtonMarshmallow.isSelected()) {
            totalHarga += 2000*jumlah;
            pilihanTopping = true;
        }
        
        if(!pilihanMenu) {
            JOptionPane.showMessageDialog(this, "Silahkan Pilih Menu!", "Peringatan", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        if(!pilihanTopping) {
            JOptionPane.showMessageDialog(this, "Silahkan Pilih Topping!", "Peringatan", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        if(totalHarga > 100000) {
            diskon = totalHarga * 0.05;
            totalHarga -= diskon;
            JOptionPane.showMessageDialog(this, "Anda Mendapat Diskon Sebesar 5%!");
        }
        TextFieldTotal.setText(String.valueOf(totalHarga));
    }//GEN-LAST:event_ButtnTotalActionPerformed

